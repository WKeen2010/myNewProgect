from turtle import *
from random import *

обстріляні_координати = []
знищені_палуби = 0
кількість_палуб = 0

розмір_поля = 10
всього_палуб = 20

speed(1000)
goto(20, -20)
clear()
for j in range(4):
    forward(розмір_поля * 20)
    right(90)

def draw(x, y, type):
    up()
    goto(x * 20, -y * 20)
    down()

    if type == "промах":
        color("gray")
        begin_fill()
        for j in range(4):
            forward(20)
            right(90)
        end_fill()

    elif type == "інше":
        color("black")
        begin_fill()
        for j in range(4):
            forward(20)
            right(90)
        end_fill()

def outline_x1(корабельні_палуби, x, y):
    if x + 1 < розмір_поля + 1 and y - 1 > 0:
        обстріляні_координати.append(str(x + 1) + " | " + str(y - 1))
        draw(x + 1, y - 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if x - i > 0 and y - 1 > 0:
            обстріляні_координати.append(str(x - i) + " | " + str(y - 1))
            draw(x - i, y - 1, "промах")

    if x + 1 < розмір_поля + 1:
        обстріляні_координати.append(str(x + 1) + " | " + str(y))
        draw(x + 1, y, "промах")
    if x - корабельні_палуби > 0:
        обстріляні_координати.append(str(x - корабельні_палуби) + " | " + str(y))
        draw(x - корабельні_палуби, y, "промах")

    if x + 1 < розмір_поля + 1 and y + 1 < розмір_поля + 1:
        обстріляні_координати.append(str(x + 1) + " | " + str(y + 1))
        draw(x + 1, y + 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if x - i > 0 and y + 1 < розмір_поля + 1:
            обстріляні_координати.append(str(x - i) + " | " + str(y + 1))
            draw(x - i, y + 1, "промах")


def outline_x2(корабельні_палуби, x, y):
    if x - 1 > 0 and y - 1 > 0:
        обстріляні_координати.append(str(x - 1) + " | " + str(y - 1))
        draw(x - 1, y - 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if x + i < розмір_поля + 1 and y - 1 > 0:
            обстріляні_координати.append(str(x + i) + " | " + str(y - 1))
            draw(x + i, y - 1, "промах")

    if x - 1 > 0:
        обстріляні_координати.append(str(x - 1) + " | " + str(y))
        draw(x - 1, y, "промах")
    if x + корабельні_палуби < розмір_поля + 1:
        обстріляні_координати.append(str(x + корабельні_палуби) + " | " + str(y))
        draw(x + корабельні_палуби, y, "промах")

    if x - 1 > 0 and y + 1 < розмір_поля + 1:
        обстріляні_координати.append(str(x - 1) + " | " + str(y + 1))
        draw(x - 1, y + 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if x + i < розмір_поля + 1 and y + 1 < розмір_поля + 1:
            обстріляні_координати.append(str(x + i) + " | " + str(y + 1))
            draw(x + i, y + 1, "промах")


def outline_y1(корабельні_палуби, x, y):
    if y + 1 < розмір_поля + 1 and x - 1 > 0:
        обстріляні_координати.append(str(x - 1) + " | " + str(y + 1))
        draw(x - 1, y + 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if y - i > 0 and x - 1 > 0:
            обстріляні_координати.append(str(x - 1) + " | " + str(y - i))
            draw(x - 1, y - i, "промах")

    if y + 1 < розмір_поля + 1:
        обстріляні_координати.append(str(x) + " | " + str(y + 1))
        draw(x, y + 1, "промах")
    if y - корабельні_палуби > 0:
        обстріляні_координати.append(str(x) + " | " + str(y - корабельні_палуби))
        draw(x, y - корабельні_палуби, "промах")

    if x + 1 < розмір_поля + 1 and y + 1 < розмір_поля + 1:
        обстріляні_координати.append(str(x + 1) + " | " + str(y + 1))
        draw(x + 1, y + 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if y - i > 0 and x + 1 < розмір_поля + 1:
            обстріляні_координати.append(str(x + 1) + " | " + str(y - i))
            draw(x + 1, y - i, "промах")


def outline_y2(корабельні_палуби, x, y):
    if x - 1 > 0 and y - 1 > 0:
        обстріляні_координати.append(str(x - 1) + " | " + str(y - 1))
        draw(x - 1, y - 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if x - 1 < розмір_поля + 1 and y + i > 0:
            обстріляні_координати.append(str(x - 1) + " | " + str(y + i))
            draw(x - 1, y + i, "промах")

    if y - 1 > 0:
        обстріляні_координати.append(str(x) + " | " + str(y - 1))
        draw(x, y - 1, "промах")
    if y + корабельні_палуби > 0:
        обстріляні_координати.append(str(x) + " | " + str(y + корабельні_палуби))
        draw(x, y + корабельні_палуби, "промах")

    if y - 1 > 0 and x + 1 < розмір_поля + 1:
        обстріляні_координати.append(str(x + 1) + " | " + str(y - 1))
        draw(x + 1, y - 1, "промах")
    i = 0
    for i in range(корабельні_палуби + 1):
        if y + i < розмір_поля + 1 and x + 1 < розмір_поля + 1:
            обстріляні_координати.append(str(x + 1) + " | " + str(y + i))
            draw(x + 1, y + i, "промах")


while знищені_палуби < всього_палуб:

    дозвіл_на_биття_по_y1 = True
    дозвіл_на_биття_по_y2 = True
    дозвіл_на_биття_по_x2 = True
    x = randint(1, розмір_поля)
    y = randint(1, розмір_поля)
    i = обстріляні_координати.count(str(x) + " | " + str(y))

    while i != 0:
        x = randint(1, розмір_поля)
        y = randint(1, розмір_поля)
        i = обстріляні_координати.count(str(x) + " | " + str(y))

    обстріляні_координати.append(str(x) + " | " + str(y))
    print(x, " | ", y)

    f = input("(промах, попав, вбив): ")
    if f == "промах":
        draw(x, y, "промах")
        continue

    elif f == "вбив":
        знищені_палуби += 1
        draw(x, y, "інше")
        outline_x1(1, x, y)
        continue

    elif f == "попав":
        знищені_палуби += 1
        кількість_палуб += 1
        draw(x, y, "інше")


        дозвіл_на_биття_по_x1 = True
        x1 = x
        while дозвіл_на_биття_по_x1 == True:
            x1 += 1
            i = обстріляні_координати.count(str(x1) + " | " + str(y))
            if x1 < розмір_поля + 1 and i == 0:
                обстріляні_координати.append(str(x1) + " | " + str(y))
                print(x1, " | ", y)
                f = input("(промах, попав, вбив): ")

                if f == "промах":
                    дозвіл_на_биття_по_x1 = False
                    draw(x1, y, "промах")

                elif f == "вбив":
                    знищені_палуби += 1
                    дозвіл_на_биття_по_y1 = False
                    дозвіл_на_биття_по_y2 = False
                    дозвіл_на_биття_по_x2 = False
                    дозвіл_на_биття_по_x1 = False
                    кількість_палуб += 1
                    draw(x1, y, "інше")
                    outline_x1(кількість_палуб, x1, y)

                elif f == "попав":
                    знищені_палуби += 1
                    дозвіл_на_биття_по_y1 = False
                    дозвіл_на_биття_по_y2 = False
                    кількість_палуб += 1
                    draw(x1, y, "інше")
                    continue
            else:
                дозвіл_на_биття_по_x1 = False

        x2 = x
        while дозвіл_на_биття_по_x2 == True:
            x2 -= 1
            i = обстріляні_координати.count(str(x2) + " | " + str(y))
            if x2 > 0 and i == 0:
                обстріляні_координати.append(str(x2) + " | " + str(y))
                print(x2, " | ", y)
                f = input("(промах, попав, вбив): ")

                if f == "промах":
                    дозвіл_на_биття_по_x2 = False
                    draw(x2, y, "промах")

                elif f == "вбив":
                    знищені_палуби += 1
                    дозвіл_на_биття_по_y1 = False
                    дозвіл_на_биття_по_y2 = False
                    дозвіл_на_биття_по_x2 = False
                    кількість_палуб += 1
                    draw(x2, y, "інше")
                    outline_x2(кількість_палуб, x2, y)

                elif f == "попав":
                    знищені_палуби += 1
                    кількість_палуб += 1
                    дозвіл_на_биття_по_y1 = False
                    дозвіл_на_биття_по_y2 = False
                    draw(x2, y, "інше")
                    continue
            else:
                дозвіл_на_биття_по_x2 = False

        y1 = y
        while дозвіл_на_биття_по_y1 == True:
            y1 += 1
            i = обстріляні_координати.count(str(x) + " | " + str(y1))
            if y1 < розмір_поля + 1 and i == 0:
                обстріляні_координати.append(str(x) + " | " + str(y1))
                print(x, " | ", y1)
                f = input("(промах, попав, вбив): ")

                if f == "промах":
                    дозвіл_на_биття_по_y1 = False
                    draw(x, y1, "промах")

                elif f == "вбив":
                    знищені_палуби += 1
                    дозвіл_на_биття_по_y1 = False
                    дозвіл_на_биття_по_y2 = False
                    кількість_палуб += 1
                    draw(x, y1, "інше")
                    outline_y1(кількість_палуб, x, y1)

                elif f == "попав":
                        знищені_палуби += 1
                        кількість_палуб += 1
                        draw(x, y1, "інше")
                        continue
            else:
                дозвіл_на_биття_по_y1 = False

        y2 = y
        while дозвіл_на_биття_по_y2 == True:
            y2 -= 1
            i = обстріляні_координати.count(str(x) + " | " + str(y2))
            if y2 > 0 and i == 0:
                обстріляні_координати.append(str(x) + " | " + str(y2))
                print(x, " | ", y2)
                f = input("(промах, попав, вбив): ")

                if f == "промах":
                    дозвіл_на_биття_по_y2 = False
                    draw(x, y2, "промах")

                elif f == "вбив":
                    знищені_палуби += 1
                    дозвіл_на_биття_по_y2 = False
                    кількість_палуб += 1
                    draw(x, y2, "інше")
                    outline_y2(кількість_палуб, x, y2)

                elif f == "попав":
                    знищені_палуби += 1
                    кількість_палуб += 1
                    draw(x, y2, "інше")
                    continue
            else:
                дозвіл_на_биття_по_y2 = False
    else:
        print("Ти грав неправильно!")
        break
    кількість_палуб = 0

